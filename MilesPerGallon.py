# Formula: MPG = Miles driven / Gallons of gas used
# Input number of miles driven
miles_driven = float(input('Enter miles driven: '))

# Input gallons of gas used
gallons_of_gas_used = float(input('Enter gallons of gas used: '))

# Calculate miles driven / gallons of gas used
print('Gallons of gas used:', (miles_driven / gallons_of_gas_used))
